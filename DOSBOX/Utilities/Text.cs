        public static void DisplayText(string text, vec v, int layer) => DisplayText(text, v.x, v.y, layer);
        public static void DisplayText(string text, int x, int y, int layer)
        {
            foreach (char c in text)
            {
                var b = GetBytesFromChar(c);
                if (b != null)
                    Display(b, x, y, layer);
                x += 5;
            }
        }

